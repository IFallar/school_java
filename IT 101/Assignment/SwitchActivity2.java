public class SwitchActivity2 {
    public static void main(String[] args) {
        int x = 30;

        switch(x){
            case 10:
                System.out.println("num is 10");
                break;
            case 20:
                System.out.println("num is 20");
                break;
            case 30:
                System.out.println("num is 30");
                break;
            default:
                System.out.println("invalid");
        }
    }

}